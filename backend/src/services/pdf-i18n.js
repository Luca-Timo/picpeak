function t(locale, key, vars = {}) {
  const dict = LABELS[locale] || LABELS.en;
  let str = dict[key] || LABELS.en[key] || key;
  for (const [k, v] of Object.entries(vars)) {
    str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
  }
  return str;
}

